
# las tuplas se declaran
tupla = (1,3,4,5, 1)
print(dir(tupla))

# obtiene un valor de la tupla mediante su indice
print(tupla[1])

# cuenta la cantidad de elementos que se repiten en una tupla por el valor ingresado
print(tupla.count(1))

# cuenta la cantidad de elementos que existen en una tupla
print(len(tupla))